export function getCrossReference(repoName) {
    return githubCrossReferences[repoName] || null;
}


export function getFeaturedGithubProjects() {
    return Object.entries(githubCrossReferences)
        .filter(([_, ref]) => ref.featured)
        .map(([repoName, _]) => repoName);
}


export function getProjectsByBootcamp(bootcamp) {
    return Object.entries(githubCrossReferences)
        .filter(([_, ref]) => ref.bootcampProject === bootcamp)
        .map(([repoName, _]) => repoName);
}


export function getAwardWinningProjects() {
    return Object.entries(githubCrossReferences)
        .filter(([_, ref]) => ref.award)
        .map(([repoName, ref]) => ({ repoName, award: ref.award }));
}